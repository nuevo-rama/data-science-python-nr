import numpy as np

# Scalar operations

scalarOperation = np.array([[1,2,3],[-1,-2,-3]])
print("Scalar Operation '+' : ", scalarOperation + 2)

scalarOperation = np.array([[1,2,3],[-1,-2,-3]])
print("Scalar Operation '-' : ", scalarOperation - 2)

scalarOperation = np.array([[1,2,3],[-1,-2,-3]])
print("Scalar Operation '*' : ", scalarOperation * 2)

scalarOperation = np.array([[1,2,3],[-1,-2,-3]])
print("Scalar Operation '/' : ", scalarOperation / 2) # Float
print("Scalar Operation '//' : ", scalarOperation // 2) # Int

scalarOperation = np.array([[1,2,3],[-1,-2,-3]])
print("Scalar Operation '**2' : ", scalarOperation **2)
