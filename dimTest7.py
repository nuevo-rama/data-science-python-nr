import numpy as np

# min() Returns the minimun value of the array
matrixOne = np.array([[1,2,3],[4,5,6],[0,0,0]])
print(np.min(matrixOne))

# max() Returns the maximum value of the array
matrixOne = np.array([[1,2,3],[4,5,6],[0,0,0]])
print(np.max(matrixOne))

# sum() Returns the sum of all values
matrixOne = np.array([[1,2,3],[4,5,6],[0,0,0]])
print(np.sum(matrixOne))

# mean() Returns the mean of all values
matrixOne = np.array([[1,2,3],[4,5,6],[0,0,0]])
print(np.mean(matrixOne))

# std() Returns the standard deviation
matrixOne = np.array([[1,2,3],[4,5,6],[0,0,0]])
print(np.std(matrixOne))

# median() Returns the median
matrixOne = np.array([[1,2,3],[4,5,6],[0,0,0]])
print(np.median(matrixOne))













